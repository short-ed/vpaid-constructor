// Имитация печатания с различным интервалом
function startTyping(text: string, index = 0) {
  if (index < text.length) {
    typingText.value += text[index]
    const randomInterval = Math.random() * 100 + 50 // Интервал от 50 до 150 миллисекунд
    timeoutId = window.setTimeout(() => startTyping(text, index + 1), randomInterval)
  }
  else {
    if (timeoutId)
